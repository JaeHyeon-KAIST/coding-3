"""
experiments/make_hof_wrapper.py — Auto-generate minicontest/zoo_reflex_O<N>.py
HOF (Hall-of-Fame) wrappers from evolve.py final_weights.py archives.

Systematic extension of pm20's manually-written zoo_reflex_A1.py pattern. Once
Order 2 finishes and its champion is promoted, running:

    .venv/bin/python experiments/make_hof_wrapper.py \\
        --weights experiments/artifacts/phase2_A1_B1_20dim/final_weights.py \\
        --name zoo_reflex_O2

emits `minicontest/zoo_reflex_O2.py`, a drop-in opponent that pins the Order 2
champion's W_OFF/W_DEF via the zoo_core.load_weights_override protocol. The
file is then added to Order 3+/4+ --opponents pool so each new CEM run must
beat every prior champion (AlphaZero-lite HOF rotation, Codex +15-40 ELO).

The generated wrapper imports ReflexTunedAgent (not A1's wrapper) to avoid
transitive weight-loading coupling. Each HOF wrapper owns its own weights path
and fails gracefully to seed-weight fallback if the weights file is missing
at runtime.

Usage:
    python experiments/make_hof_wrapper.py --weights <path> --name <module>
        [--class <ClassName>] [--minicontest <dir>] [--force]

Exit codes:
  0 = wrote file successfully
  1 = weights missing or malformed
  2 = output module already exists and --force not set
"""

from __future__ import annotations

import argparse
import importlib.util
import sys
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent
MINICONTEST = REPO_ROOT / "minicontest"

TEMPLATE = '''# {out_name}.py  (auto-generated by experiments/make_hof_wrapper.py)
# ----------------------------------------------------------------------
# HOF (Hall-of-Fame) opponent wrapper: ReflexTunedAgent pinned to a
# previously-evolved champion's weights. Used as opponent in later
# evolve.py Order pools so new populations must out-perform this champion
# (AlphaZero-lite coevolution rotation).
#
# Provenance: {weights_provenance}
# Number of W_OFF entries: {n_w_off}
# Number of W_DEF entries: {n_w_def}
#
# Never submitted; minicontest-only training opponent.

from __future__ import annotations

from pathlib import Path

from zoo_reflex_tuned import ReflexTunedAgent

_REPO_ROOT = Path(__file__).resolve().parent.parent

# Try multiple candidate paths for robustness across Mac/server layouts.
_WEIGHT_PATHS = [
{weight_paths_block}
]


def _load_override():
    """Load W_OFF / W_DEF from the weights archive. Never raises."""
    import importlib.util
    import sys as _sys
    for path in _WEIGHT_PATHS:
        if not path.exists():
            continue
        try:
            spec = importlib.util.spec_from_file_location("_hof_weights", str(path))
            mod = importlib.util.module_from_spec(spec)
            spec.loader.exec_module(mod)
            w_off = dict(getattr(mod, "W_OFF", {{}}))
            w_def_raw = dict(getattr(mod, "W_DEF", {{}}))
            w_def = w_def_raw or None
            return {{
                "w_off": w_off,
                "w_def": w_def,
                "params": dict(getattr(mod, "PARAMS", {{}})),
            }}
        except Exception as exc:
            try:
                print(f"[{out_name}] warn: load failed from {{path}}: {{exc}}",
                      file=_sys.stderr)
            except Exception:
                pass
            continue
    try:
        print(
            f"[{out_name}] warn: no weights file found; falling back to seed weights",
            file=_sys.stderr,
        )
    except Exception:
        pass
    return {{"w_off": {{}}, "w_def": None, "params": {{}}}}


_OVERRIDE = _load_override()


class {class_name}(ReflexTunedAgent):
    """ReflexTunedAgent with {class_name} evolved weights pinned at construction."""

    def __init__(self, index, timeForComputing=0.1):
        super().__init__(index, timeForComputing=timeForComputing)
        if _OVERRIDE.get("w_off"):
            self._weights_override = _OVERRIDE


def createTeam(firstIndex, secondIndex, isRed,
               first="{class_name}", second="{class_name}"):
    return [{class_name}(firstIndex), {class_name}(secondIndex)]
'''


def _derive_class_name(module_name: str) -> str:
    """'zoo_reflex_O2' -> 'ReflexO2Agent'. Falls back to capitalizing parts."""
    # Strip 'zoo_' prefix if present.
    stem = module_name
    if stem.startswith("zoo_"):
        stem = stem[len("zoo_"):]
    # CamelCase each underscore-separated token.
    parts = [p for p in stem.split("_") if p]
    camel = "".join(p[:1].upper() + p[1:] for p in parts)
    if not camel.endswith("Agent"):
        camel = camel + "Agent"
    return camel


def _validate_weights(path: Path) -> tuple[int, int]:
    """Import the weights file in an isolated namespace and return
    (len(W_OFF), len(W_DEF)). Exits 1 on any failure.
    """
    if not path.exists():
        print(f"[make_hof_wrapper] ERROR: weights file does not exist: {path}",
              file=sys.stderr)
        sys.exit(1)
    try:
        spec = importlib.util.spec_from_file_location("_validate_weights", str(path))
        mod = importlib.util.module_from_spec(spec)
        spec.loader.exec_module(mod)
    except Exception as exc:
        print(f"[make_hof_wrapper] ERROR: failed to import {path}: {exc}",
              file=sys.stderr)
        sys.exit(1)
    if not hasattr(mod, "W_OFF") or not hasattr(mod, "W_DEF"):
        print(f"[make_hof_wrapper] ERROR: {path} missing W_OFF or W_DEF globals",
              file=sys.stderr)
        sys.exit(1)
    w_off = getattr(mod, "W_OFF", None) or {}
    w_def = getattr(mod, "W_DEF", None) or {}
    if not isinstance(w_off, dict) or not isinstance(w_def, dict):
        print(f"[make_hof_wrapper] ERROR: W_OFF/W_DEF must be dicts in {path}",
              file=sys.stderr)
        sys.exit(1)
    return len(w_off), len(w_def)


def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--weights", required=True, type=Path,
                    help="evolve.py-emitted final_weights.py file")
    ap.add_argument("--name", required=True,
                    help="Output module name stem (e.g. 'zoo_reflex_O2')")
    ap.add_argument("--class", dest="class_name", default=None,
                    help="Override class name (default derived from --name)")
    ap.add_argument("--minicontest", type=Path, default=MINICONTEST,
                    help="Output directory (default: minicontest/)")
    ap.add_argument("--force", action="store_true",
                    help="Overwrite existing module file")
    args = ap.parse_args()

    weights_abs = args.weights.resolve()
    n_w_off, n_w_def = _validate_weights(weights_abs)

    class_name = args.class_name or _derive_class_name(args.name)
    out_path = args.minicontest / f"{args.name}.py"
    if out_path.exists() and not args.force:
        print(f"[make_hof_wrapper] ERROR: {out_path} exists (use --force to overwrite)",
              file=sys.stderr)
        return 2

    # Build candidate-paths block: first entry = absolute path we saw; second =
    # repo-relative path (for server/Mac portability if the archive gets scp'd).
    try:
        repo_rel = weights_abs.relative_to(REPO_ROOT)
        rel_path_str = repr(str(repo_rel))
        weight_paths_block = (
            f"    _REPO_ROOT / {rel_path_str},\n"
            f"    Path({repr(str(weights_abs))}),"
        )
    except ValueError:
        weight_paths_block = f"    Path({repr(str(weights_abs))}),"

    provenance = f"{weights_abs} (W_OFF: {n_w_off} keys, W_DEF: {n_w_def} keys)"

    content = TEMPLATE.format(
        out_name=args.name,
        class_name=class_name,
        weights_provenance=provenance,
        n_w_off=n_w_off,
        n_w_def=n_w_def,
        weight_paths_block=weight_paths_block,
    )
    out_path.write_text(content)
    print(f"[make_hof_wrapper] wrote {out_path} (class: {class_name}, "
          f"W_OFF={n_w_off}, W_DEF={n_w_def})")
    return 0


if __name__ == "__main__":
    sys.exit(main())
